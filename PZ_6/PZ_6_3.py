#Дан список размера N. Осуществить сдвиг элементов список вправо на одну
#позицию (при этом A1 перейдет в A2, A2 — в A3, ..., AN-1 — в AN, a исходное значение
#последнего элемента будет потеряно). Первый элемент полученного списк

def shift_right():
    """Сдвигает элементы списка вправо и заменяет первый элемент на 0."""
    try:
        n = int(input("Введите размер списка N: "))
        a = []
        for i in range(n):
            a.append(int(input(f"Введите элемент {i + 1}: ")))
        
        a = [0] + a[:-1]
        print("Результирующий список:", a)
    
    except ValueError:
        print("Ошибка: введите целые числа.")
        shift_right()

shift_right()
